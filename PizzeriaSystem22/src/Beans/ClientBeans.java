public class ClientBeans {
    
    private int codCli;
    private String nameCli;
    private String adressCli;
    private String districtCli;
    private String stateCli;
    private String phoneCli;
    private String emailCli;
    private String zipCli;
    private String dateCli;
      
      
    public ClientBeans() {
    }

    public int getCodCli() {
        return codCli;
    }

    public void setCodCli(int codCli) {
        this.codCli = codCli;
    }

    public String getNameCli() {
        return nameCli;
    }

    public void setNameCli(String nameCli) {
        this.nameCli = nameCli;
    }

    public String getAdressCli() {
        return adressCli;
    }

    public void setAdressCli(String adressCli) {
        this.adressCli = adressCli;
    }

    public String getDistrictCli() {
        return districtCli;
    }

    public void setDistrictCli(String districtCli) {
        this.districtCli = districtCli;
    }

    public String getStateCli() {
        return stateCli;
    }

    public void setStateCli(String stateCli) {
        this.stateCli = stateCli;
    }

    public String getPhoneCli() {
        return phoneCli;
    }

    public void setPhoneCli(String phoneCli) {
        this.phoneCli = phoneCli;
    }

    public String getEmailCli() {
        return emailCli;
    }

    public void setEmailCli(String emailCli) {
        this.emailCli = emailCli;
    }

    public String getZipCli() {
        return zipCli;
    }

    public void setZipCli(String zipCli) {
        this.zipCli = zipCli;
    }

    public String getDateCli() {
        return dateCli;
    }

    public void setDateCli(String dateCli) {
        this.dateCli = dateCli;
    }     
    
}
